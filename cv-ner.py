def personnaliser_descriptif(descriptif_base, index_descriptif):
    """Personnalise un descriptif avec des valeurs déterministes"""

    if "équipe de {} personnes" in descriptif_base:
        valeur = 3 + (index_descriptif % 28)  # Équipes de 3 à 30 personnes
        return descriptif_base.format(valeur)

    elif "{}K€" in descriptif_base:
        valeur = 10 + (index_descriptif * 5) % 491  # Budgets de 10K€ à 500K€
        return descriptif_base.format(valeur)

    elif "{}M€" in descriptif_base:
        valeur = 1 + (index_descriptif % 50)  # Budgets de 1M€ à 50M€
        return descriptif_base.format(valeur)

    elif "{}%" in descriptif_base:
        valeur = 5 + (index_descriptif % 36)  # Pourcentages de 5% à 40%
        return descriptif_base.format(valeur)

    elif "événements {} avec {} participants" in descriptif_base:
        types = ["professionnels", "grand public", "internes", "internationaux", "sectoriels", "thématiques", "corporate"]
        type_choisi = types[index_descriptif % len(types)]
        nombre = 50 + (index_descriptif * 10) % 951  # Entre 50 et 1000 participants
        return descriptif_base.format(type_choisi, nombre)

    elif "campagnes digitales {} sur {} réseaux sociaux" in descriptif_base:
        types = ["performantes", "ciblées", "virales", "innovantes", "personnalisées", "automatisées"]
        type_choisi = types[index_descriptif % len(types)]
        nombre = 3 + (index_descriptif % 13)  # Entre 3 et 15 réseaux sociaux
        return descriptif_base.format(type_choisi, nombre)

    else:
        # Cas général
        complements = [
            "qualité", "performance", "innovation", "développement durable",
            "transformation digitale", "amélioration continue", "service client",
            "internationaux", "nationaux", "régionaux", "stratégiques", "opérationnels"
        ]

        # Vérifier combien de placeholders contient la chaîne
        nb_placeholders = descriptif_base.count("{}")

        if nb_placeholders == 1:
            return descriptif_base.format(complements[index_descriptif % len(complements)])
        else:
            args = []
            for i in range(nb_placeholders):
                args.append(complements[(index_descriptif + i) % len(complements)])
            return descriptif_base.format(*args)


