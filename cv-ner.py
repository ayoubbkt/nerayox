def save_data(data, output_path):
   """Sauvegarde les données au format spaCy avec gestion améliorée des entités"""
   nlp = spacy.blank("fr")
   db = DocBin()

   for text, annot in data:
       doc = nlp.make_doc(text)
       ents = []

       # Tri des entités par importance (priorité) et longueur
       entity_priorities = {
           "NOM": 1, "EMAIL": 2, "TELEPHONE": 3, "ADRESSE": 4,
           "ENTREPRISE": 5, "POSTE": 6, "DUREE": 7, "COMPETENCE": 8,
           "FORMATION": 9, "ECOLE": 10, "ANNEE_ACADEMIQUE": 11,
           "VILLE": 12, "PAYS": 13
       }

       # Trier d'abord par priorité, puis par longueur (descendante)
       sorted_entities = sorted(annot["entities"],
                              key=lambda x: (entity_priorities.get(x[2], 100), -(x[1] - x[0])))

       # Garder trace des positions déjà utilisées
       used_positions = set()

       for start, end, label in sorted_entities:
           # Vérifier si le span chevauche un span existant
           span_range = set(range(start, end))
           if not span_range.intersection(used_positions):
               span = doc.char_span(start, end, label=label, alignment_mode="strict")
               if span is not None:
                   ents.append(span)
                   # Marquer les positions comme utilisées
                   used_positions.update(span_range)

       doc.ents = ents
       db.add(doc)

   db.to_disk(output_path)



def generer_donnees_entrainement(n_cv):
    """Génère un ensemble de données d'entraînement avec n_cv CV différents"""
    # Générer les CV de manière déterministe
    cv_data = [generer_cv(i) for i in range(n_cv)]


    # Diviser en ensembles d'entraînement et de test
    train_data, test_data = train_test_split(cv_data, test_size=0.2, random_state=42)

    # Sauvegarder les données
    save_data(train_data, "train_cv_ecoles.spacy")
    save_data(test_data, "test_cv_ecoles.spacy")

    return "train_cv_ecoles.spacy", "test_cv_ecoles.spacy"

